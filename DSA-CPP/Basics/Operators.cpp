//Operators

#include<iostream>
using namespace std;

int main()
{
    int a=2,b=4;
    cout<<"a&b="<<(a&b)<<endl;
    cout<<"a|b="<<(a|b)<<endl;
    cout<<"~a="<<(~a)<<endl;
    cout<<"a^b="<<(a^b)<<endl;

    cout<<"17>>1="<<(17>>1)<<endl;
    cout<<"17>>2="<<(17>>2)<<endl;
    cout<<"19<<1="<<(19<<1)<<endl;
    cout<<"21<<1="<<(21<<1)<<endl;

    return 0;
}

/*
a&b=0
a|b=6
~a=-3
a^b=6
17>>1=8
17>>2=4
19<<1=38
21<<1=42
*/

